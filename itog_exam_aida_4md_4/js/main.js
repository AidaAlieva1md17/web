// –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–ª–∏–∫–∞ –ø–æ —Ñ–∏–ª—å—Ç—Ä—É
function handleFilterClick() {
    // –§—É–Ω–∫—Ü–∏—è, –∫–æ—Ç–æ—Ä–∞—è –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –ø—Ä–∏ –∫–ª–∏–∫–µ –Ω–∞ –ª—é–±—É—é –∫–Ω–æ–ø–∫—É —Ñ–∏–ª—å—Ç—Ä–∞
    // –ö–ª—é—á–µ–≤–æ–µ —Å–ª–æ–≤–æ `this` –≤–Ω—É—Ç—Ä–∏ —Ñ—É–Ω–∫—Ü–∏–∏ —Å—Å—ã–ª–∞–µ—Ç—Å—è –Ω–∞ —ç–ª–µ–º–µ–Ω—Ç, –ø–æ –∫–æ—Ç–æ—Ä–æ–º—É –∫–ª–∏–∫–Ω—É–ª–∏
    
    const type = this.getAttribute("data-type");
    // –ü–æ–ª—É—á–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –∞—Ç—Ä–∏–±—É—Ç–∞ "data-type" —É –Ω–∞–∂–∞—Ç–æ–π –∫–Ω–æ–ø–∫–∏
    // –≠—Ç–æ —Ç–∏–ø —Ñ–∏–ª—å—Ç—Ä–∞: "category", "technology" –∏–ª–∏ "complexity"
    // –ê—Ç—Ä–∏–±—É—Ç –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω –≤ HTML –∫–Ω–æ–ø–∫–∏, –Ω–∞–ø—Ä–∏–º–µ—Ä: <button data-type="category">
    
    const value = this.getAttribute("data-filter");
    // –ü–æ–ª—É—á–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –∞—Ç—Ä–∏–±—É—Ç–∞ "data-filter" —É –Ω–∞–∂–∞—Ç–æ–π –∫–Ω–æ–ø–∫–∏
    // –≠—Ç–æ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–∞, –Ω–∞–ø—Ä–∏–º–µ—Ä: "3D", "js", "Beginner"
    
    // –û–±–Ω–æ–≤–ª—è–µ–º —Ñ–∏–ª—å—Ç—Ä
    filters[type] = value;
    // –û–±–Ω–æ–≤–ª—è–µ–º —Å–≤–æ–π—Å—Ç–≤–æ –≤ –æ–±—ä–µ–∫—Ç–µ `filters` –ø–æ –∫–ª—é—á—É `type`
    // –ù–∞–ø—Ä–∏–º–µ—Ä: filters["category"] = "3D"
    // –≠—Ç–æ –∏–∑–º–µ–Ω—è–µ—Ç —Ç–µ–∫—É—â–∏–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ —Ñ–∏–ª—å—Ç—Ä–æ–≤
    
    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ LocalStorage
    localStorage.setItem("portfolioFilters", JSON.stringify(filters));
    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ–±—ä–µ–∫—Ç `filters` –≤ –ª–æ–∫–∞–ª—å–Ω–æ–µ —Ö—Ä–∞–Ω–∏–ª–∏—â–µ –±—Ä–∞—É–∑–µ—Ä–∞
    // `JSON.stringify()` –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –æ–±—ä–µ–∫—Ç JavaScript –≤ —Å—Ç—Ä–æ–∫—É JSON –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è
    // –≠—Ç–æ –ø–æ–∑–≤–æ–ª–∏—Ç –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —Ñ–∏–ª—å—Ç—Ä—ã –ø—Ä–∏ —Å–ª–µ–¥—É—é—â–µ–º –ø–æ—Å–µ—â–µ–Ω–∏–∏ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏
    setActiveButtons();
    // –í—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é, –∫–æ—Ç–æ—Ä–∞—è –æ–±–Ω–æ–≤–∏—Ç –≤–∏–∑—É–∞–ª—å–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ –≤—Å–µ—Ö –∫–Ω–æ–ø–æ–∫
    // –ü–æ–¥—Å–≤–µ—Ç–∏—Ç –∞–∫—Ç–∏–≤–Ω—É—é –∫–Ω–æ–ø–∫—É –∏ —Å–±—Ä–æ—Å–∏—Ç –ø–æ–¥—Å–≤–µ—Ç–∫—É —Å –æ—Å—Ç–∞–ª—å–Ω—ã—Ö
    
    // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø—Ä–æ–µ–∫—Ç—ã
    showProjects();
    // –ü–µ—Ä–µ—Ä–∏—Å–æ–≤—ã–≤–∞–µ–º –ø—Ä–æ–µ–∫—Ç—ã —Å —É—á–µ—Ç–æ–º –Ω–æ–≤—ã—Ö —Ñ–∏–ª—å—Ç—Ä–æ–≤
}

// –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∞–∫—Ç–∏–≤–Ω—ã–µ –∫–Ω–æ–ø–∫–∏
function setActiveButtons() {
    // –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –æ—Ç–≤–µ—á–∞–µ—Ç –∑–∞ –≤–∏–∑—É–∞–ª—å–Ω–æ–µ –≤—ã–¥–µ–ª–µ–Ω–∏–µ –∞–∫—Ç–∏–≤–Ω—ã—Ö –∫–Ω–æ–ø–æ–∫ —Ñ–∏–ª—å—Ç—Ä–æ–≤
    
    const buttons = document.querySelectorAll(".filter-btn");
    // –°–Ω–æ–≤–∞ –Ω–∞—Ö–æ–¥–∏–º –í–°–ï –∫–Ω–æ–ø–∫–∏ —Ñ–∏–ª—å—Ç—Ä–æ–≤ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
    
    buttons.forEach(btn => {
        // –î–ª—è –∫–∞–∂–¥–æ–π –∫–Ω–æ–ø–∫–∏ –≤—ã–ø–æ–ª–Ω—è–µ–º —Ñ—É–Ω–∫—Ü–∏—é
        
        const type = btn.getAttribute("data-type");
        // –ü–æ–ª—É—á–∞–µ–º —Ç–∏–ø —Ñ–∏–ª—å—Ç—Ä–∞ –∫–Ω–æ–ø–∫–∏
        
        const value = btn.getAttribute("data-filter");
        // –ü–æ–ª—É—á–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–∞ –∫–Ω–æ–ø–∫–∏
        
        btn.classList.remove("active");
        // –£–¥–∞–ª—è–µ–º CSS-–∫–ª–∞—Å—Å "active" —É –∫–Ω–æ–ø–∫–∏
        // –≠—Ç–æ —Å–±—Ä–∞—Å—ã–≤–∞–µ—Ç –ø–æ–¥—Å–≤–µ—Ç–∫—É, –µ—Å–ª–∏ –æ–Ω–∞ –±—ã–ª–∞
        
        if (filters[type] === value) {
            // –ü—Ä–æ–≤–µ—Ä—è–µ–º: –µ—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–∞ –≤ –æ–±—ä–µ–∫—Ç–µ `filters` 
            // —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å–æ –∑–Ω–∞—á–µ–Ω–∏–µ–º —ç—Ç–æ–π –∫–Ω–æ–ø–∫–∏
            
            btn.classList.add("active");
            // –î–æ–±–∞–≤–ª—è–µ–º CSS-–∫–ª–∞—Å—Å "active" –∫–Ω–æ–ø–∫–µ
            // –≠—Ç–æ –≤–∏–∑—É–∞–ª—å–Ω–æ –≤—ã–¥–µ–ª—è–µ—Ç –∫–Ω–æ–ø–∫—É –∫–∞–∫ –∞–∫—Ç–∏–≤–Ω—É—é
        }
    });
}

// –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –ø—Ä–æ–µ–∫—Ç—ã
