    // 2. –ü–æ–ª—É—á–∞–µ–º –µ–≥–æ —Ç–µ–∫—Å—Ç–æ–≤–æ–µ —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ (—ç—Ç–æ JSON-—Å—Ç—Ä–æ–∫–∞ —Å –¥–∞–Ω–Ω—ã–º–∏ –æ –ø—Ä–æ–µ–∫—Ç–∞—Ö)
    // 3. –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º JSON-—Å—Ç—Ä–æ–∫—É –≤ –º–∞—Å—Å–∏–≤ –æ–±—ä–µ–∫—Ç–æ–≤ JavaScript —Å –ø–æ–º–æ—â—å—é JSON.parse()
