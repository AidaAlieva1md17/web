        // –í `filteredProjects` –ø–æ–ø–∞–¥—É—Ç —Ç–æ–ª—å–∫–æ —Ç–µ –ø—Ä–æ–µ–∫—Ç—ã, –¥–ª—è –∫–æ—Ç–æ—Ä—ã—Ö —Ñ—É–Ω–∫—Ü–∏—è –≤–µ—Ä–Ω–µ—Ç `true`
        // `project` - —Ç–µ–∫—É—â–∏–π —ç–ª–µ–º–µ–Ω—Ç –º–∞—Å—Å–∏–≤–∞ –≤ –∏—Ç–µ—Ä–∞—Ü–∏–∏
        
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏—é
