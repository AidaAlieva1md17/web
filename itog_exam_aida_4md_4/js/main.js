        // –ï—Å–ª–∏ –≤—Å–µ –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø—Ä–æ–π–¥–µ–Ω—ã (–∏–ª–∏ —Ñ–∏–ª—å—Ç—Ä—ã —Å—Ç–æ—è—Ç –Ω–∞ "all")
